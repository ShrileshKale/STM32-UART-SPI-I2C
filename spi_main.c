int main(void)
{

	// transmit data buffer creation
	uint8_t txBuff[] = {"Hi from STM32"};

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();
  /* Configure the system clock */
  SystemClock_Config();
  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_SPI1_Init();

  // Deactivate Slave
  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_6, GPIO_PIN_SET);
  HAL_Delay(10);

  // Transmit Buffer to slave device
  Spi_Transmit(txBuff);


}


static void Spi_Transmit(uint8_t *txBuff)
{

	uint8_t RxBuffer[1];

	HAL_GPIO_WritePin(GPIOB, GPIO_PIN_6, GPIO_PIN_RESET);
	HAL_SPI_Transmit(&hspi1, txBuff, (uint8_t*) strlen(txBuff),100);
	Spi_Receive(RxBuffer);
	HAL_GPIO_WritePin(GPIOB, GPIO_PIN_6, GPIO_PIN_SET);

}

static void Spi_Receive(uint8_t *buff)
{
	uint8_t AckBuffer[] = {"Received"};
	uint8_t NotAckBuffer[] = {"Not Received"};


	if ( (HAL_SPI_Receive(&hspi1, &buff, strlen(buff), 100)) == HAL_OK)
	{
		HAL_Delay(7000); // slave device accept data after 5sec
		HAL_SPI_Transmit(&hspi1, (uint8_t*)AckBuffer, strlen(AckBuffer),100);
	}
	else
	{
		HAL_Delay(7000); // slave device accept data after 5sec
		HAL_SPI_Transmit(&hspi1, (uint8_t*)NotAckBuffer, strlen(NotAckBuffer),100);
	}

}


